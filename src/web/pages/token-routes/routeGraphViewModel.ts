export function getNodeCardSubtitle(node: RouteGraphNode): string {
  if (node.type === 'entry') {
    const match = (node.match || {}) as Record<string, unknown>;
    return String(match.displayName || match.requestedModelPattern || tr('pages.tokenRoutes.routeGraphViewModel.publicModelEntry'));
  }
  if (node.type === 'dispatcher') {
    return formatValue('pages.tokenRoutes.routeGraphViewModel.dispatcherSubtitle', {
      mode: getDispatcherModeLabel(node.mode || 'route'),
    });
  }
  if (node.type === 'route_endpoint') {
    const config = (node.config || {}) as { targets?: unknown[] };
    const targetCount = Array.isArray(config.targets) ? config.targets.length : 0;
    const kind = String(node.endpointKind || (targetCount > 0 ? 'supply' : 'route_product'));
    const exposure = String(node.exposure || (kind === 'supply' ? 'none' : node.visibility));
    if (kind === 'supply') {
      return targetCount === 1
        ? tr('pages.tokenRoutes.routeGraphViewModel.oneUpstreamTarget')
        : formatCount('pages.tokenRoutes.routeGraphViewModel.upstreamTargetsCount', targetCount);
    }
    return `${getEndpointKindLabel(kind)} · ${getMappedLabel(visibilityLabelKeys, exposure) || exposure}`;
  }
  if (node.type === 'filter') {
    const count = Array.isArray(node.operations) ? node.operations.length : 0;
    return count === 1
      ? tr('pages.tokenRoutes.routeGraphViewModel.oneOperation')
      : formatCount('pages.tokenRoutes.routeGraphViewModel.operationsCount', count);
  }
  if (node.type === 'synthetic_endpoint') {
    return formatValue('pages.tokenRoutes.routeGraphViewModel.syntheticResponse', {
      status: String(node.statusCode || 503),
    });
  }
  return formatValue('pages.tokenRoutes.routeGraphViewModel.ownershipNode', {
    ownership: getOwnershipLabel(node.ownership),
  });
}

export function getNodeCardMetrics(graph: RouteGraphLike, node: RouteGraphNode): string[] {
  const metrics: string[] = [];
  const connectionCount = getNodeConnectionCount(graph, node.id);
  metrics.push(connectionCount === 1
    ? tr('pages.tokenRoutes.routeGraphViewModel.oneConnection')
    : formatCount('pages.tokenRoutes.routeGraphViewModel.connectionsCount', connectionCount));
  if (typeof node.successRate === 'number') {
    metrics.push(formatValue('pages.tokenRoutes.routeGraphViewModel.successPercent', {
      percent: Math.round(node.successRate * 100),
    }));
  }
  if (node.type === 'dispatcher') metrics.push(String(node.policy && typeof node.policy === 'object' && 'strategy' in node.policy ? (node.policy as any).strategy : 'weighted'));
  return metrics;
}

