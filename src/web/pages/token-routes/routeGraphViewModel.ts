export type PublicModelEntryRow = {
  id: string;
  nodeId: string;
  title: string;
  subtitle: string;
  source: 'compiled_endpoint' | 'entry_node' | 'macro_entry';
};

function formatPublicModelCatalogSubtitle(endpoint: RouteEndpointCatalogItem): string {
  const parts = [
    getMappedLabel(ownerKindLabelKeys, endpoint.ownerKind),
    endpoint.siteNames.slice(0, 2).join(', '),
    endpoint.sourceRouteIds && endpoint.sourceRouteIds.length > 0
      ? formatCount('pages.tokenRoutes.routeGraphViewModel.routesCount', endpoint.sourceRouteIds.length)
      : '',
    endpoint.endpointId,
  ].filter(Boolean);
  return parts.join(' · ');
}

function getRecordValue(value: unknown): Record<string, unknown> | null {
  return value && typeof value === 'object' && !Array.isArray(value) ? value as Record<string, unknown> : null;
}

function getPublicEntryNodeTitle(node: RouteGraphNode): string {
  const match = (node.match || {}) as Record<string, unknown>;
  return String(match.displayName || match.requestedModelPattern || getNodeTitle(node)).trim();
}

function getConfiguredPublicMacroEntry(macro: RouteGraphMacro): PublicModelEntryRow | null {
  if (macro.kind !== 'candidate_selector') return null;
  const config = getRecordValue(macro.config);
  const surface = getRecordValue(config?.surface);
  const entry = getRecordValue(surface?.entry);
  if (!entry || entry.kind !== 'external' || entry.visibility !== 'public') return null;
  const match = getRecordValue(entry.match);
  const title = String(match?.displayName || match?.requestedModelPattern || macro.name || macro.id).trim();
  if (!title) return null;
  const enabled = macro.enabled !== false;
  return {
    id: enabled ? `macro:${macro.id}:entry` : `macro-config:${macro.id}:entry`,
    nodeId: enabled ? `macro:${macro.id}:entry` : macroFlowNodeId(macro.id),
    title,
    subtitle: `${enabled ? '' : `${tr('pages.tokenRoutes.routeGraphViewModel.status.disabled')} · `}${tr('pages.tokenRoutes.routeGraphViewModel.ownerKind.macro')} · ${macro.id}`,
    source: 'macro_entry',
  };
}

export function getPublicModelEntryRows(
  graph: RouteGraphLike,
  endpointCatalog: RouteEndpointCatalogItem[] = [],
): PublicModelEntryRow[] {
  const rows = new Map<string, PublicModelEntryRow>();
  const seenTitles = new Set<string>();

  for (const endpoint of endpointCatalog) {
    if (endpoint.enabled === false) continue;
    if (endpoint.endpointKind !== 'route_product') continue;
    if (endpoint.exposure !== 'public') continue;
    if (endpoint.resolutionStatus === 'unresolved') continue;
    const title = String(endpoint.publicModelName || endpoint.modelPattern || endpoint.label || '').trim();
    if (!title) continue;
    const titleKey = title.toLowerCase();
    seenTitles.add(titleKey);
    rows.set(`endpoint:${endpoint.endpointId}`, {
      id: endpoint.endpointId,
      nodeId: endpoint.nodeId || endpoint.endpointId,
      title,
      subtitle: formatPublicModelCatalogSubtitle(endpoint),
      source: 'compiled_endpoint',
    });
  }

  for (const node of getPublicEntryNodes(graph)) {
    const title = getPublicEntryNodeTitle(node);
    if (!title) continue;
    const titleKey = title.toLowerCase();
    if (seenTitles.has(titleKey)) continue;
    seenTitles.add(titleKey);
    rows.set(`node:${node.id}`, {
      id: node.id,
      nodeId: node.id,
      title,
      subtitle: getModelListSubtitle(node),
      source: 'entry_node',
    });
  }

  for (const macro of graph.macros || []) {
    const row = getConfiguredPublicMacroEntry(macro);
    if (!row) continue;
    const titleKey = row.title.toLowerCase();
    if (seenTitles.has(titleKey)) continue;
    seenTitles.add(titleKey);
    rows.set(row.id, row);
  }

  return Array.from(rows.values()).sort((left, right) => {
    const titleOrder = left.title.localeCompare(right.title);
    return titleOrder || left.id.localeCompare(right.id);
  });
}

export function filterPublicModelEntryRows(rows: PublicModelEntryRow[], query: string): PublicModelEntryRow[] {
  const terms = query
    .trim()
    .toLowerCase()
    .split(/\s+/)
    .filter(Boolean);
  if (terms.length === 0) return rows;
  return rows.filter((row) => {
    const haystack = [
      row.title,
      row.subtitle,
      row.id,
      row.nodeId,
      row.source,
    ].join(' ').toLowerCase();
    return terms.every((term) => haystack.includes(term));
  });
}

export function getManualNodeCount(graph: RouteGraphLike): number {
  return graph.nodes.filter((node) => node.ownership === 'manual').length;
}

export function getNodeConnectionCount(graph: RouteGraphLike, nodeId: string): number {
  return graph.edges.filter((edge) => edge.sourceNodeId === nodeId || edge.targetNodeId === nodeId).length;
}

export function getPortConnectionCount(graph: RouteGraphLike, nodeId: string, portId: string): number {
  return graph.edges.filter((edge) => (
    (edge.sourceNodeId === nodeId && edge.sourcePortId === portId)
    || (edge.targetNodeId === nodeId && edge.targetPortId === portId)
  )).length;
}

